	<p>La crittografia <em>simmertica</em> si basa sulla condivisione di un <em>segreto</em>
	tra mittente e ricevente, chiamato <em>chiave</em>.</p>

	<p>Utilizzando la chiave, il mittente genera un <em>messaggio cifrato</em>, incomprensibile
	all'attaccante, a partire dal messaggio in chiaro (cifratura). Utilizzando la chiave, il ricevente
	<em>decodifica</em> il messaggio cifrato per ottenere quello in chiaro (decifrazione).</p>


